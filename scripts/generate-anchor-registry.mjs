import { readFileSync, writeFileSync } from 'node:fs';
import { createHash } from 'node:crypto';
import { classifyHeading } from '../src/domain/concepts.mjs';

const raw = readFileSync(new URL('../src/content/landing.md', import.meta.url), 'utf8');
const headings = [...raw.matchAll(/^(#{1,4})\s+(.+?)\s*$/gm)].map((match) => ({ depth: match[1].length, title: match[2] }));
const seen = new Map();
const registry = {};
const role = (title, depth) => {
  if (depth === 1) return 'overview';
  if (/قیمت|هزینه/.test(title)) return 'price';
  if (/عوارض|خطر|هشدار|فوری|انسداد|اورژانس/.test(title)) return 'safety';
  if (/کاندید|مناسب|چه کسانی|نباید|رد شود|مکث/.test(title)) return 'candidacy';
  if (/مراقبت|بعد از|نقاهت/.test(title)) return 'aftercare';
  if (/ماندگاری|شروع اثر|دوام/.test(title)) return 'duration';
  if (/فرق|مقایسه|بهتر است یا|یکی نیست/.test(title)) return 'comparison';
  if (/جراحی|ارجاع|رینوپلاستی|بلفارو|فیس.?لیفت|کاشت مو/.test(title)) return 'surgery-boundary';
  if (/قبلی|ناموفق|اصلاح|مهاجرت|ترمیم/.test(title)) return 'revision';
  if (/[؟?]$/.test(title) || depth === 4) return 'answer';
  if (/چگونه|چرا|مکانیسم|لایه|تشخیص/.test(title)) return 'mechanism';
  return depth === 2 ? 'guide' : 'detail';
};
for (const heading of headings) {
  const group = classifyHeading(heading.title, heading.depth).groupId || 'aesthetic';
  const base = `${group}-${role(heading.title, heading.depth)}`.replace(/[^a-z0-9-]/g, '-').replace(/-+/g, '-');
  const occurrence = (seen.get(base) ?? 0) + 1;
  seen.set(base, occurrence);
  const suffix = occurrence === 1 ? '' : `-${occurrence}`;
  const id = `${base}${suffix}`.slice(0, 72).replace(/-$/, '');
  registry[heading.title] = id || `section-${createHash('sha1').update(heading.title).digest('hex').slice(0, 10)}`;
}
const output = `// Generated by scripts/generate-anchor-registry.mjs. Preserve existing values.\nexport const anchorRegistry: Record<string, string> = ${JSON.stringify(registry, null, 2)};\n`;
writeFileSync(new URL('../src/domain/anchors.ts', import.meta.url), output);

